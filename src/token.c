#include "token.h"

/** Map TokenKind -> display string.  Used by diagnostics and --dump-tokens. */
const char *token_kind_string(TokenKind kind) {
    static const char *names[] = {
        [TOKEN_INTEGER_LITERAL] = "INTEGER_LITERAL",
        [TOKEN_FLOAT_LITERAL] = "FLOAT_LITERAL",
        [TOKEN_STRING_LITERAL] = "STRING_LITERAL",
        [TOKEN_TRUE] = "true",
        [TOKEN_FALSE] = "false",
        [TOKEN_IDENTIFIER] = "IDENTIFIER",
        [TOKEN_MODULE] = "module",
        [TOKEN_PUBLIC] = "pub",
        [TOKEN_FUNCTION] = "fn",
        [TOKEN_VARIABLE] = "var",
        [TOKEN_IF] = "if",
        [TOKEN_ELSE] = "else",
        [TOKEN_LOOP] = "loop",
        [TOKEN_FOR] = "for",
        [TOKEN_BREAK] = "break",
        [TOKEN_CONTINUE] = "continue",
        [TOKEN_ASSERT] = "assert",
        [TOKEN_BOOL] = "bool",
        [TOKEN_I32] = "i32",
        [TOKEN_U32] = "u32",
        [TOKEN_F64] = "f64",
        [TOKEN_STRING] = "str",
        [TOKEN_UNIT] = "unit",
        [TOKEN_PLUS] = "+",
        [TOKEN_MINUS] = "-",
        [TOKEN_STAR] = "*",
        [TOKEN_SLASH] = "/",
        [TOKEN_PERCENT] = "%",
        [TOKEN_EQUAL_EQUAL] = "==",
        [TOKEN_BANG_EQUAL] = "!=",
        [TOKEN_LESS] = "<",
        [TOKEN_LESS_EQUAL] = "<=",
        [TOKEN_GREATER] = ">",
        [TOKEN_GREATER_EQUAL] = ">=",
        [TOKEN_AMPERSAND_AMPERSAND] = "&&",
        [TOKEN_PIPE_PIPE] = "||",
        [TOKEN_BANG] = "!",
        [TOKEN_PIPE] = "|",
        [TOKEN_COLON_EQUAL] = ":=",
        [TOKEN_EQUAL] = "=",
        [TOKEN_PLUS_EQUAL] = "+=",
        [TOKEN_MINUS_EQUAL] = "-=",
        [TOKEN_STAR_EQUAL] = "*=",
        [TOKEN_SLASH_EQUAL] = "/=",
        [TOKEN_LEFT_PAREN] = "(",
        [TOKEN_RIGHT_PAREN] = ")",
        [TOKEN_LEFT_BRACE] = "{",
        [TOKEN_RIGHT_BRACE] = "}",
        [TOKEN_COLON] = ":",
        [TOKEN_COMMA] = ",",
        [TOKEN_DOT_DOT] = "..",
        [TOKEN_DOT] = ".",
        [TOKEN_ARROW] = "->",
        [TOKEN_SEMICOLON] = ";",
        [TOKEN_INTERPOLATION_START] = "INTERPOLATION_START",
        [TOKEN_INTERPOLATION_END] = "INTERPOLATION_END",
        [TOKEN_NEWLINE] = "NEWLINE",
        [TOKEN_EOF] = "EOF",
        [TOKEN_ERROR] = "ERROR",
    };
    if (kind >= 0 && kind < (int32_t)(sizeof(names) / sizeof(names[0])) && names[kind] != NULL) {
        return names[kind];
    }
    return "?";
}
